"""Eval: trace_id and session_id propagation via AgentRuntime.invoke.

Verifies that after AgentRuntime.invoke completes:
- A trace_id is set in the structlog ContextVar (a valid UUID4).
- A session_id is bound when the request carries one.
- Each invocation generates a fresh trace_id (verified by comparing two calls
  to set_trace_id() directly — ContextVar isolation is guaranteed by
  AgentRuntime.invoke calling bind_request_context on every call).
"""

from __future__ import annotations

import uuid

import pytest

from agent.runtime.agent_runtime import AgentRuntime
from agent.schemas import MacroSnapshotSummaryRequest, SignalReviewRequest
from agent.service import AgentService
from core.logging.logger import get_session_id, get_trace_id
from services.macro_service import MacroService
from services.signal_service import SignalService


def _make_runtime() -> AgentRuntime:
    """Build an AgentRuntime that calls bind_request_context on every invoke."""
    macro_svc = MacroService()
    signal_svc = SignalService()
    service = AgentService(macro_svc, signal_svc)
    return AgentRuntime(service)


@pytest.mark.asyncio
class TestTraceIdPropagation:
    """trace_id and session_id are set in ContextVars by AgentRuntime.invoke."""

    async def test_trace_id_is_set_after_invoke(self) -> None:
        """After invoke(), get_trace_id() returns a non-empty valid UUID string."""
        runtime = _make_runtime()
        request = MacroSnapshotSummaryRequest(
            request_id="trace-test-001",
            country="US",
        )
        await runtime.invoke(request)

        trace_id = get_trace_id()
        assert trace_id != ""
        uuid.UUID(trace_id)  # raises ValueError if not a valid UUID

    async def test_trace_id_is_a_valid_uuid4(self) -> None:
        runtime = _make_runtime()
        request = MacroSnapshotSummaryRequest(
            request_id="trace-test-002",
            country="US",
        )
        await runtime.invoke(request)
        trace_id = get_trace_id()
        parsed = uuid.UUID(trace_id)
        assert parsed.version == 4

    async def test_each_invoke_sets_fresh_trace_id(self) -> None:
        """Each invocation calls set_trace_id(), which generates a new UUID4.

        We verify this by collecting two UUIDs generated by set_trace_id() and
        checking they differ (extremely low probability of collision with UUID4).
        """
        from core.logging.logger import set_trace_id

        t1 = set_trace_id()
        t2 = set_trace_id()
        assert t1 != t2, "set_trace_id() must produce a fresh UUID on each call"

    async def test_session_id_is_set_in_contextvar_when_provided(self) -> None:
        runtime = _make_runtime()
        request = MacroSnapshotSummaryRequest(
            request_id="trace-test-sess",
            session_id="test-session-999",
            country="US",
        )
        await runtime.invoke(request)
        assert get_session_id() == "test-session-999"

    async def test_session_id_empty_when_not_provided(self) -> None:
        runtime = _make_runtime()
        request = MacroSnapshotSummaryRequest(
            request_id="trace-test-no-sess",
            country="US",
        )
        await runtime.invoke(request)
        # session_id defaults to None → bind_request_context leaves it empty
        session_id = get_session_id()
        assert session_id == "" or session_id is None or session_id == "None"

    async def test_trace_id_set_for_signal_review_request(self) -> None:
        runtime = _make_runtime()
        request = SignalReviewRequest(
            request_id="trace-sig-001",
            signal_ids=["bull_market"],
            country="US",
        )
        await runtime.invoke(request)
        trace_id = get_trace_id()
        assert trace_id != ""
        uuid.UUID(trace_id)
